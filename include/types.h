#pragma once

typedef unsigned char uint8_t;          // Define 8bit (1 Byte) Integer Type
typedef unsigned short uint16_t;        // Define 16bit (2 Byte) Integer Type
typedef unsigned int uint32_t;          // Define 32bit (4 Byte) Integer Type
typedef unsigned long long uint64_t;    // Define 64bit (8 Byte) Integer Type
typedef uint32_t size_t;                // Unsigned Integer Type Used for Sizes and Memory-Related Operations (For 32bit Mode)

typedef unsigned char bool;             // Define Boolean Type

#define true 1                          // Define True Symbol as 1
#define false 0                         // Define False Symbol as 0

#define null ((void*)0)                 // Define Null Symbol as Void